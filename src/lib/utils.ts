export function getColors( str ) {
	if ( !str || typeof str !== 'string' ) {
		return null;
	}

	return ( str.match( /(#[a-f0-9]{3,6})|rgba?\(\s?(([0-9\.]+),?\s?){3,4}\s?\)/gmi ) || [] );
}
